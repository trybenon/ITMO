package console;

/**
 * Интерфейс для ввода и вывода данных.
 * Содержит методы для считывания различных типов данных и записи в поток вывода.
 */
public interface ReaderWriter {

    /**
     * Читает целое число.
     *
     * @return считанное целое число
     */
    int readInt();

    /**
     * Читает целое число.
     *
     * @return считанное целое число
     */
    long readLong();

    /**
     * Читает число с плавающей точкой.
     *
     * @return считанное число с плавающей точкой
     */
    float readFloat();

    /**
     * Читает строку.
     *
     * @return считанную строку
     */
    String readLine();

    /**
     * Записывает строку в поток вывода.
     *
     * @param text строка для вывода
     */
    void write(String text);

    /**
     * Получает и валидирует значение от пользователя.
     *
     * @param message сообщение перед вводом
     * @return валидированное значение
     */
    String getValidatedValue(String message);
}
